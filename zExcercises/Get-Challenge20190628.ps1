A POWERSHELL CRYPTOGRAM
The Chairman is back with another challenge, this one probably more on the
harder end. Although once you get your head around how to solve it, you’ll
probably find it pretty easy. Your challenge is to solve a cryptogram like
this, presumably using PowerShell.
$crypto = @"
g - 2 5 R e t o p c 7 -
h v 1 Q n e l b e d E p
"@
Inside this string block is a hidden message. To solve or decrypt, you need a
pair of numbers, X and Y. Starting in the upper left you would start counting X
number of characters, starting at 1. Once you have the first letter then count Y
number of characters for the 2nd letter. Then X number of characters for the 3rd
letter and so on. The end result will be a word or phrase with no spaces. But it
should be recognizable to you
In this sample, the solution is 1,5. You would start counting at 1 for the first
letter. Then count 5 for the next, 1 for the next and so on.
Do you see the hidden message?
Ready for a challenge? Find the hidden message in this cryptogram. You’ll have
to discover key intervals. It could be 2,6 or 3,8 or 6,2. To make it easier, you
do not have to loop around.
$crypto = @"
P k T r 2 s z 2 * c F -
r a z 7 G u D 4 w 6 U #
g c t K 3 E @ B t 1 a Y
Q P i c % 7 0 5 Z v A e
W 6 j e P R f p m I ) H
y ^ L o o w C n b J d O
S i 9 M b e r # ) i e U
* f 2 Z 6 M S h 7 V u D
5 a ( h s v 8 e l 1 o W
Z O 7 l p K y J l D z $
- j I @ t T 2 3 R a i k
q = F & w B 6 c % H l y
"@
Once you discover the correct intervals, the message should be self-evident.
In order to give everyone a chance to solve this, please don’t submit any answers
as comments. They won’t be published. But you are welcome to submit links to your
work and solution.
#>

