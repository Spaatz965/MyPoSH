#
# 1. Connect to APP1 using the Azure portal and its local administrator account (APP1\<local admin account name>).
# 2. Open a Windows PowerShell command prompt.
#

# Join APP1 to the CORP domain
Add-Computer -DomainName corp.contoso.com
Restart-Computer

#
# 1. Connect to APP1 using the Azure portal and the User1 account (CORP\User1).
# 2. Open an administrator-level Windows PowerShell command prompt.
#

# Add IIS
Install-WindowsFeature Web-WebServer -IncludeManagementTools

# Create a shared folder and a text file within the folder
New-Item -path c:\files -type directory
Write-Output "This is a shared file." | out-file c:\files\example.txt
New-SmbShare -name files -path c:\files -changeaccess CORP\User1

